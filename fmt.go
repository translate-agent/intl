func defaultNumberingSystem(locale language.Tag) numberingSystem {
	lang, script, region := locale.Raw()

	switch script {
	case latn:
		return numberingSystemLatn
	case adlm:
		return numberingSystemAdlm
	}

	switch lang {
	default:
		return numberingSystemLatn
	case ar:
		switch region {
		default:
			return numberingSystemArab
		case regionAE, regionDZ, regionEH, regionLY, regionMA, regionTN:
			return numberingSystemLatn
		}
	case ckb, sdh:
		return numberingSystemArab
	case sd:
		if script == deva {
			return numberingSystemLatn
		}

		return numberingSystemArab
	case as, bn, mni:
		return numberingSystemBeng
	case bgn, fa, lrc, mzn, ps:
		return numberingSystemArabext
	case bgc, bho, mr, ne, raj, sa:
		return numberingSystemDeva
	case ccp:
		return numberingSystemCakm
	case dz:
		return numberingSystemTibt
	case ks:
		if script == deva {
			return numberingSystemLatn
		}

		return numberingSystemArabext
	case hnj:
		return numberingSystemHmnp
	case my:
		return numberingSystemMymr
	case nqo:
		return numberingSystemNkoo
	case pa:
		if script == arab {
			return numberingSystemArabext
		}

		return numberingSystemLatn
	case sat:
		return numberingSystemOlck
	case ur:
		if region == regionIN {
			return numberingSystemArabext
		}

		return numberingSystemLatn
	case uz:
		if script == arab {
			return numberingSystemArabext
		}

		return numberingSystemLatn
	}
}

func defaultCalendar(locale language.Tag) calendarType {
	region, _ := locale.Region()

	switch region {
	default:
		return calendarTypeGregorian
	case regionAF:
		return calendarTypePersian
	case regionIR:
		if lang, _ := locale.Base(); lang == az {
			return calendarTypeGregorian
		}

		return calendarTypePersian
	case regionSA:
		return calendarTypeIslamicUmalqura
	case regionTH:
		return calendarTypeBuddhist
	}
}

func unitName(locale language.Tag) fields {
	lang, _ := locale.Base()
	s := lang.String()

	if v, confidence := locale.Script(); confidence == language.Exact {
		s += "-" + v.String()

		v, ok := fieldsLookup[s]
		if ok {
			return v
		}
	}

	if v, confidence := locale.Region(); confidence == language.Exact {
		s += "-" + v.String()
	}

	v, ok := fieldsLookup[s]
	if ok {
		return v
	}

	v, ok = fieldsLookup[lang.String()]
	if ok {
		return v
	}

	return fields{
		Month: "Month",
		Day:   "Day",
	}
}

func dayName(locale language.Tag) string {
	return unitName(locale).Day
}
